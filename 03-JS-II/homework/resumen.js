/*
  
   For:

   - Se trata de un bucle que permite iterar en un rango de condiciones específicas:

   for (let i = 0; i < 7; i++) {} // en este caso, el bucle inicia el conteo en 0, dejará de contar en i = 7

   La mayor parte del software se ejecuta en búcles, se evaluan expresiones varias veces hasta 
   obtener el resultado buscado. JS tiene el loop "for" y el loop "while" incorporados. Tienen ambos
   una sintaxis específica. 

   Operadores lógicos (&&, ||, !):

   && es AND. Evalúa dos expresiones booleanas (a && b), las evalúa para "true". Ambas deben ser true. 
   Si "a" o "b" resultan falsas, con una sola que sea falsa, ya el resultado será "false" y se evalúan en orden,
   por lo que si "a" es falsa, ese statement devuelve "false" y no evalúa "b".


   || es OR. Evalúa dos expresione booleanas (a || b), las evalúa para "true". Si una es "a" es true y
   "b" es false, el statement será true. Se evalúa en orden.


   ! es NOT. Se niega un booleano, de forma tal que si "a" es false, el operador NOT se encargará de invertir
   el booleano, por lo que "a" que era "false" se evaluará como "true". Es muy importante cuando se quiere
   utilizar esta notación en conjunto con los demás operadores lógicos.

   Al aplicar operadores lógicos, debe utilizarse paréntesis, para facilitar la evaluación de todo lo que 
   se halla por dentro de estos.


*/