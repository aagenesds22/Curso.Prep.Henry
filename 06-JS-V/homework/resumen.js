/*

    Prototype:

    El prototipo es una propiedad de la clase Object, es decir, está presente en todas las clases creadas por el usuario
    ya que reciben la propiedad "prototype" por herencia. Casi todos los objetos en JS son una instancia de Object, de forma tal
    que una instancia de Clase creada por el usuario, hereda un Object.prototype. Esto es accesible por todos los demás objetos,
    a través de la prototype chaining, por lo que un cambio del prototipo afecta a toda la cadena de objetos. También es altamente
    poderoso, ya que puede extenderse el comportamiento de plantilla al número deseado de objetos a crearse.

    
    
    Classes:

    Las clases constituyen una herramienta muy útil para crear objetos que compartan algunas propiedades y
    métodos. Son un tipo de función especial, con una sintaxis particular: declaraciones class y expresiones
    class. Poseen una propiedad prototipo con la que el usuario puede interactuar creando métodos y propiedades. 


*/